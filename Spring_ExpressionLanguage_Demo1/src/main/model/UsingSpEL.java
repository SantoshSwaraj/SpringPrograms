package main.model;

public class UsingSpEL {
	private boolean equalCheck;
	private String concat;
	private double exponent;
	/**
	 * @return the equalCheck
	 */
	public boolean getEqualCheck() {
		return equalCheck;
	}
	/**
	 * @param equalCheck the equalCheck to set
	 */
	public void setEqualCheck(boolean equalCheck) {
		this.equalCheck = equalCheck;
	}
	/**
	 * @return the concat
	 */
	public String getConcat() {
		return concat;
	}
	/**
	 * @param concat the concat to set
	 */
	public void setConcat(String concat) {
		this.concat = concat;
	}
	/**
	 * @return the exponent
	 */
	public double getExponent() {
		return exponent;
	}
	/**
	 * @param exponent the exponent to set
	 */
	public void setExponent(double exponent) {
		this.exponent = exponent;
	}
	
}
